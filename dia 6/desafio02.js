//CRIANDO UM CONTADOR QUE CONTA 5 EM 5
console.log("Contagem 5 em 5")
for(let contador = 0; contador <= 50; contador += 5) {
    console.log(contador)   
}

//CRIANDO CONTADOR INVERSA 5 EM 5
console.log("Inversa 5 em 5")
for(let contador = 50; contador >= 0; contador -= 5) {
    console.log(contador)
}